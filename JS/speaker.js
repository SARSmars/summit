const container = document.getElementById("fixed_section");
const numLines = 10 ; // Number of lines for both directions

// Create vertical grid lines
for (let i = 1; i < numLines; i++) {
  const verticalLine = document.createElement("div");
  verticalLine.classList.add("line", "vertical");
  verticalLine.style.left = `${(i / numLines) * 100}%`;
  container.appendChild(verticalLine);
}

// Create horizontal grid lines
for (let i = 1; i < numLines; i++) {
  const horizontalLine = document.createElement("div");
  horizontalLine.classList.add("line", "horizontal");
  horizontalLine.style.top = `${(i / numLines) * 100}%`;
  container.appendChild(horizontalLine);
}
